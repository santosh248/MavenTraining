package com.jenkins.Training;

public class MyCalculator {
	
	//Method to two Add Numbers
	public int addNumbers(int x, int y){
		return x + y;
	}
	
	//Method to two Subtract Numbers
	public int subtractNumbers(int x, int y){
		return x - y;
	}
	
	//Method to two multiply Numbers
	public int multiplyNumbers(int x, int y){
		return x * y;
	}
	//Method to two divide Numbers
	public int divideNumbers(int x, int y){
		return x / y;
	}
	
}
